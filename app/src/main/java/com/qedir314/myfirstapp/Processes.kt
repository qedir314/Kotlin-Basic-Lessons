package com.qedir314.myfirstapp

class Processes {

    fun substraction(x: Int, y: Int) : Int {
        return x - y
    }

    fun substraction(x: Int, y: Int, z: Int) : Int {
        return x - (y + z)
    }

    fun substraction(x: Int, y: Int, z: Int, o: Int) : Int {
        return x - (y + z + o)
    }
}